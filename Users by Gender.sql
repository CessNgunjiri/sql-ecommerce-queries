--- Users by Age Group
SELECT 
    CASE 
        WHEN age < 20 THEN 'Under 20'
        WHEN age BETWEEN 20 AND 29 THEN '20-29'
        WHEN age BETWEEN 30 AND 39 THEN '30-39'
        WHEN age BETWEEN 40 AND 49 THEN '40-49'
        WHEN age BETWEEN 50 AND 59 THEN '50-59'
        ELSE '60+'
    END AS age_group,
    COUNT(*) AS users
FROM users
GROUP BY age_group
ORDER BY users DESC;


--- Users By Gender
SELECT 
    gender,
    COUNT(*) AS users
FROM users
GROUP BY gender;
